<h3 id="transparency">2. Evaluate the brand’s ingredient transparency</h3>
<p>How easy is it to locate a FULL list of ingredients? Not “key ingredients,” but the full list! Sometimes websites hide this information on a separate page; at the bottom of the product page; use faint, small or otherwise obscured text; or design the page so as to require the consumer to have to go through multiple clicks to get a complete list. This is a red flag! We advise that you stray away from brands who make it difficult for their customers to understand the contents of the products. Products whose ingredients are unknown could contain any number of chemical toxins.</p>
<p>Also, it is not as important what is not in a product (that list could be endless) so much as what is in the product. If the company demonstrates that they do 3rd-party testing of possible contaminants, even better. If you cannot locate a full list, this is a major red flag and we cannot endorse the product/brand.</p>

