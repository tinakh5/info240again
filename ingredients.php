<h3 id="packaging">3. Packaging</h3>
<p>While plastic packaging is not a deal breaker, it would be great to stray away from products packaged in plastic bottles, pouches, tubes, jars etc. It is much more difficult to get confirmation about a brand’s packaging safety since they often do not manufacture their own packaging materials, and thus have less oversight about its quality. The plastic in packaging can contain bisphenols, benzophenones, phthalates, PFAS among other chemicals. If you cannot determine the materials used in packaging based on photos or product description, email the company to ask! Better yet, opt for zero-waste/package-free products (check out the zero-waste shopping resources I've put together <a href="shopping.php">here!</a>)</p>

