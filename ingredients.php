<h3 id="company-values">4. Company values</h3>
<p>See if a brand has published "About Us," "Our Values," and/or "Learn More" pages on their website to get a feel for their values, mission, priorities. The depth of information they provide about their manufacturing and sourcing process is a good litmus test to see how much they care about their environmental and ethical impact. This usually correlates with a more conscious selection and treatment of ingredients.</p>

