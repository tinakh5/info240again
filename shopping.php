<h2>Buyerarchy of Needs</h2>
<img src="images/needs.jpg" width="300" height="256" alt="Buyerarchy of Needs shopping pyramid">
<p class="citation">Source: Lazarovic, Sarah. (2021). <a href="https://www.sarahl.com/">https://www.sarahl.com/</a></p>
<p>Making any purchase places stress on the environment. All manufacturing consumes electricity and water, requires fuel to transport and creates waste. Sarah Lazarovic's "Buyerarchy of Needs" is a great illustration to help guide your decision-making before shopping. Buying something new should be your LAST resort! First consider if you can reuse something you already have, borrow from a friend, swap with a friend, purchase it second-hand, or make it on your own before contributing to more shopping waste. If you must purchase a new product, vote with your wallet and support businesses who make a concerted effort to minimize their environmental impact!</p>

