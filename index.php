<h2>What is the toxin-free lifestyle guide?</h2>
<p>We are exposed to thousands of chemicals in our everyday environment. There are more than 80,000 chemicals in the United States, yet less than 1,000 chemicals have been tested for human safety. Reducing exposures to toxic chemicals is essential to leading a healthier lifestyle. These toxins can interfere with proper hormone and organ functioning, thus increasing your risk of developing disease and inhibiting physical performance. Unfortunately these toxins have become ubiquitous in many <a href="#household">common household products</a>, <a href="#personalcare">personal care products</a>, <a href="#foodbev">food and beverages</a>, and the <a href="#environment">environment</a>. The toxin-free lifestyle guide can help you make informed decisions about how to avoid exposure to harmful chemicals! </p>

<h2 id="household">Household Products</h2>
<p>From frying pans to window cleaners, many common household products may expose you and your family to harmful chemicals. </p>
<p>Take action!</p>
<ul>
	<li>Flexible vinyl plastics, also known as PVC (#3), help give materials their pliable texture and shape. However, they also contain phthalates that may endanger our immunity. Replace plastic shower curtains with a linen or cotton shower curtain, wash dishes without using rubber gloves, and wear leather or vegan leather flip-flops instead of rubber ones.</li>
	<li>Candles, reed diffusers, and/or incense most likely contain phthalates due to the common ingredient “parfum/fragrance." Additionally, candle burning may also generate PM2.5, an air pollutant.</li>
	<li>Toilet paper made from recycled paper may be contaminated with BPA, for recycled paper can contain receipt paper, which is printed on thermal paper. Opt for sustainably-raised bamboo toilet paper instead.</li>
	<li>Dry cleaning agents may contain fragrance formulated with phthalates. Avoid dry cleaning by air drying clothes in the sun or handwashing.</li>
	<li>Avoid “stain resistant” or “water repellent” clothing, textiles, and carpets. Avoid any product that purports to be be "non-stick" (e.g. pots, pans, baking sheets, aluminum foil, and other kitchenware). These are likely to be coated using PFAS, a group of chemicals used to make something non-stick or smooth. PFAS are used in numerous consumer products to offer waterproof, greaseproof, stain-proof and non-stick properties. PFAS have been shown to produce severe adverse effects on the endocrine, immune, and metabolic systems. </li>
</ul>

<h2 id="personalcare">Personal Care Products</h2>
<p>Many beauty and personal care products contain harmful chemicals and contaminants that are often not tested and not confirmed to be safe.</p>
<p>Take action!</p>
<ul>
	<li>Avoid products with the ingredient “Fragrance/Parfum." Phthalates (DBP, DEHP, DEP and others) can be found in synthetic fragrance, nail polish, and hairspray to make the scents stick to skin. Synthetic flavor or fragrances are engineered scents or flavoring agents that may contain any combination of 3,000+ stock chemical ingredients, including hormone disruptors and allergens. Fragrance formulas are protected under federal law's classification of trade secrets and therefore can remain undisclosed.</li>
	<li>Pay attention to ingredient labels in soaps, detergents, shampoos, toothpaste, lotions, cosmetics etc.</li>
	<li>Opt for organic cotton and/or medical-grade silicone feminine care products. There are studies that found PFAS, nanosilver, and chlorine bleach in many popular period care products.</li>
</ul>

<h2 id="foodbev">Food and Beverage</h2>
<p>From the plastic packaging on the food we eat to the water we drink, many people do not know that we are doing more than simply using plastic; we are ingesting it too. 96% of human bodies have BPA, a plastic chemical used to make packaging. BPA can contaminate water and food. When consumed, BPA mimics estrogen and disrupts normal hormone functioning.</p>
<p>Take action!</p>
<ul>
	<li>As much as possible, use BPA-free products. Avoid polycarbonate containers (clear, hard plastic). Do not microwave or heat polycarbonate plastic food containers. BPA-free products do not mean that they are free of other types of BPA, such as BPS and BPF, which may affect our bodies the same way as BPA.</li>
	<li>Avoid plastic and aluminum cups/mugs/water bottles.  Plastic containers may leach BPA and BPA-alternatives into your beverage. The plastic lining in aluminum cans may do the same. Drink beverages in glass, ceramic, and/or stainless steel containers.</li>
	<li>Avoid single-use plastic straws and utensils. Opt for stainless steel and/or glass straws and stainless steel, wooden and/or bamboo utensils.</li>
	<li>Avoid single-use plastic containers when getting take-out food from restaurants or going grocery shopping. Bring your own glass and/or stainless steel tupperware to hold food to-go. Opt for package-less or glass/paper-packaged grocery store items.</li>
	<li>Avoid single-use plastic baggies (e.g. Ziploc bags) and Saran wrap may contain phthalates, which can leach into your food. Opt for glass, beeswax wrap, silicone and/or stainless steel food storage containers.</li>
	<li>Avoid fast food and take-out. Plastic utensils, food served in plastic packaging, and/or food prepared from plastic packaging may expose you to BPA. Eat more fresh food and less processed and packaged food.</li>
	<li>Avoid packaged food and snacks. This includes frozen foods, microwave/boxed meals, energy bars, and canned/plastic-bottled beverages. Even the lining on paper and aluminum food containers can contain BPA.</li>
	<li>Avoid canned ingredients when cooking. The plastic lining in aluminum cans may leach BPA and BPA-alternatives into the food.</li>
</ul>

<h2 id="environment">Environment</h2>
<p>Everyday, we are exposed to toxic chemicals in environments where we live, work, and play.</p>
<p>Take action!</p>
<ul>
	<li>Get a water filtration system. Approximately 200 million people are drinking water with PFAS in it. These forever chemicals have been linked to infertility in adults and neurodevelopment issues in babies.</li>
	<li>Avoid plastic-toys; opt for wooden, cloth, or medical-grade silicone ones</li>
	<li>Avoid handling CDs and DVDs, for they can contain BPA. Make sure you wash your hands thoroughly right after handling</li>
	<li>Exercise caution when purchasing commodities with a <a href="https://oehha.ca.gov/proposition-65" target="_blank">Prop 65</a> warning label (or, if shopping online, in the product description section). Proposition 65 requires California to publish a list of more than 900 chemicals known to cause cancer or reproductive toxicity.</li>
	<li>BPA and phthalates can also come out of products and collect in dust. Remember to wash your hands often, especially before preparing or eating food. Clean your floors regularly, using a wet mop or HEPA vacuum if possible. Use a damp cloth to dust surfaces.</li>
	<li>Thermal cash receipts can contain BPA and BPA-alternatives. Opt for digital receipts instead and have them emailed or texted to you.</li>
	
</ul>

